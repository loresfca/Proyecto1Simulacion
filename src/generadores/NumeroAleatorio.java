package generadores;

public class NumeroAleatorio {
	private int xi;
	private double ri;
	
	public NumeroAleatorio(int xi, double ri){
		this.xi=xi;
		this.ri=ri;
	}
	
	public int getXi() {
		return xi;
	}

	public void setXi(int xi) {
		this.xi = xi;
	}

	public double getRi() {
		return ri;
	}

	public void setRi(double ri) {
		this.ri = ri;
	}
}
